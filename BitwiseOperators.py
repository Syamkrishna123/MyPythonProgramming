i=50 #110010
j=30 #11110
#bitwise and
print(i&j) #010010 =18
#bitwise or
print(i|j) #111110 =62
#bitwise not
print(~i) #one's complement -(110011)= -51
#bitwise XOR
print(i^j)#101100=44
#bitwise right shift
print(i>>3) #110=6
#bitwise left shift
print(i<<2) #11001000 = 200
